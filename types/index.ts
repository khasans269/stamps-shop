// ─── Теги ────────────────────────────────────────────────────────────────
// Теги — это дополнительные «сквозные» характеристики товара поверх
// категории. Один товар может иметь несколько тегов. Например, алфавит
// может одновременно быть в категории "alphabets-cyrillic" и иметь
// тег "with-extras" (есть цифры или знаки препинания).
//
// Зачем отдельно от категории: категория — это место в навигации
// (одно на товар), тег — необязательный фильтр, который пересекает
// категории. Один и тот же тег может встречаться у кириллицы и латиницы.

export type Tag = "with-extras";

export const TAG_NAMES: Record<Tag, string> = {
  "with-extras": "С цифрами/знаками",
};

// Порядок отображения чипсов тегов. Нужен, чтобы UI был детерминированным.
export const TAG_ORDER: Tag[] = ["with-extras"];

export interface CartItem {
  productId: string;
  quantity: number;
}

export interface Product {
  id: string;
  slug: string;
  name: string;
  price: number; // в рублях, без копеек
  category: Category;
  // Дополнительные характеристики поверх категории. Опционально:
  // если поля нет — товар просто не попадёт ни в один тег-фильтр.
  // Можно перечислить несколько: ["with-extras", "decorative"].
  tags?: Tag[];
  description: string;
  // Массив путей к фотографиям относительно /public. Порядок имеет значение:
  // первое фото — главное, оно показывается на карточке в каталоге, и
  // открывается первым на странице товара. Остальные — в галерее.
  // Если у товара нет фото — поле просто не указывается.
  images?: string[];
  inStock: boolean;
  // Скрыть товар с сайта, не удаляя его из products.json.
  // hidden: true — товар не показывается в каталоге и на своей странице
  // (отдаёт 404). Убрать поле (или false) — товар снова виден.
  hidden?: boolean;
  // Дополнительные характеристики. Опциональные — если у товара их нет,
  // поле не указывается в products.json, и на странице оно не отобразится.
  itemsInSet?: number; // количество элементов в наборе (например, 33 буквы)
  letterHeight?: string; // высота букв, строка — чтобы поддерживать "4 мм" или "4, 6 и 8 мм"
}
